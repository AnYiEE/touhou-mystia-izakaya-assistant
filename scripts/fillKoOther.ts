const filePath = 'app/data/i18n/ko.ts';
const content = readFileSync(filePath, 'utf8');

let updated = content;
let count = 0;
const notFound: string[] = [];

for (const [key, value] of Object.entries(koTranslations)) {
	const escaped = key.replace(/[.*+?^${}()|[\]\\]/g, '\\$&');
	const pattern = new RegExp(`'${escaped}':\\s*'',`);
	if (pattern.test(updated)) {
		const safeValue = value.replace(/\\/g, '\\\\').replace(/'/g, "\\'");
		updated = updated.replace(pattern, `'${key}': '${safeValue}',`);
		count++;
	} else {
		notFound.push(key);
	}
}

writeFileSync(filePath, updated, 'utf8');
console.log(`Updated ${count} translations in ${filePath}`);
if (notFound.length > 0) {
	console.log(`Not found: ${notFound.length}`);
	for (const k of notFound) console.log(`  - ${k}`);
}
