/**
 * 使用 opencc-js 将数据翻译表中的简体中文转换为繁体中文。
 * 处理所有 key（名称、标签、描述、其他），不仅仅是名称类。
 */
import * as fs from 'fs';
import * as OpenCC from 'opencc-js';

const converter = OpenCC.Converter({from: 'cn', to: 'tw'});

const filePath = 'app/data/i18n/zh-hant.ts';
const content = fs.readFileSync(filePath, 'utf8');
const lines = content.split('\n');

let changed = 0;
let unchanged = 0;
const newLines: string[] = [];

for (const line of lines) {
	// 匹配空值行: \t'key': '',
	const keyMatch = line.match(/^(\t'(.+?)':\s*)'',?$/);
	if (keyMatch) {
		const key = keyMatch[2];
		const converted = converter(key);
		if (converted !== key) {
			const newLine = line.replace("''", `'${converted.replace(/'/g, "\\'")}'`);
			newLines.push(newLine);
			changed++;
		} else {
			newLines.push(line);
			unchanged++;
		}
		continue;
	}
	newLines.push(line);
}

fs.writeFileSync(filePath, newLines.join('\n'));
console.log(`Updated ${filePath}`);
console.log(`Changed: ${changed}, Unchanged (same in both): ${unchanged}`);

// 显示一些样本
console.log('\nSample conversions (first 30 changed):');
let sampleCount = 0;
for (const line of newLines) {
	const match = line.match(/^\t'(.+?)':\s*'(.+?)',?$/);
	if (match && match[2] && sampleCount < 30) {
		console.log(`  ${match[1]} → ${match[2]}`);
		sampleCount++;
	}
}
