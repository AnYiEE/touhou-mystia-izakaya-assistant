const filePath = 'app/data/i18n/ja.ts';
const content = fs.readFileSync(filePath, 'utf8');
const lines = content.split('\n');

let changed = 0;
const newLines: string[] = [];

for (const line of lines) {
	const keyMatch = line.match(/^(\t'((?:[^'\\]|\\.)*)': )'',?$/);
	if (keyMatch) {
		const rawKey = keyMatch[2];
		const key = rawKey.replace(/\\'/g, "'");
		if (jaTranslations[key]) {
			const escaped = jaTranslations[key].replace(/\\/g, '\\\\').replace(/'/g, "\\'");
			const newLine = line.replace("''", `'${escaped}'`);
			newLines.push(newLine);
			changed++;
			continue;
		}
	}
	newLines.push(line);
}

fs.writeFileSync(filePath, newLines.join('\n'));
console.log(`Updated ${filePath}`);
console.log(`Newly translated: ${changed}`);

