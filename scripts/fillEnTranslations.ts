// 读取英文翻译文件并填充
const filePath = 'app/data/i18n/en.ts';
const content = fs.readFileSync(filePath, 'utf8');
const lines = content.split('\n');

let changed = 0;
let notFound = 0;
const missingKeys: string[] = [];
const newLines: string[] = [];

for (const line of lines) {
	const keyMatch = line.match(/^(\t'(.+?)':\s*)'',?$/);
	if (keyMatch) {
		const key = keyMatch[2];
		if (enTranslations[key]) {
			const escaped = enTranslations[key].replace(/'/g, "\\'");
			const newLine = line.replace("''", `'${escaped}'`);
			newLines.push(newLine);
			changed++;
			continue;
		} else {
			notFound++;
			missingKeys.push(key);
		}
	}
	newLines.push(line);
}

fs.writeFileSync(filePath, newLines.join('\n'));
console.log(`Updated ${filePath}`);
console.log(`Translated: ${changed}`);
console.log(`Still empty: ${notFound}`);
if (missingKeys.length > 0 && missingKeys.length <= 50) {
	console.log('\nMissing translations (first 50):');
	for (const k of missingKeys.slice(0, 50)) {
		console.log(`  '${k}'`);
	}
}
